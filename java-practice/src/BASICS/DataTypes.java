package BASICS;

public class DataTypes {
	public static void main(String[] args) {
		byte byteVar =-120;
		short shortVar = 32767;
		int intVar= 234567868;
		long longVar = 1234567899012345690L;
		float floatVar = 1903.45678F;
		double doubleVar = 12378.2345689012;
		boolean booleanVar = false;
		char charVar = 'W';
		System.out.println("Value of byte Varaiable is " + byteVar);
		System.out.println("Value of short Varaiable is " + shortVar);
		System.out.println("Value of int Varaiable is " + intVar);
		System.out.println("Value of long Varaiable is " + longVar);
		System.out.println("Value of float Varaiable is " + floatVar);
		System.out.println("Value of double Varaiable is " + doubleVar);
		System.out.println("Value of boolean Varaiable is " + booleanVar);
		System.out.println("Value of char Varaiable is " + charVar);
	}
}